public class Alumnos {
    private String nombre;
    private float nota;

    public Alumnos() {

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getNota() {
        return nota;
    }

    public void setNota(float nota) {

        this.nota = nota;
    }
}